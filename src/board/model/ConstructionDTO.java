package board.model;

public class ConstructionDTO {
	private int constNum;
	private String constName;
	private String constWay;
	private String constArea;
	private String constPrice;
	private String constLower;
	private String constOpening;
	private String constInstitution;
	private String constPercent;
	private String crtDate;
	private String udtDate;
	
	public int getConstNum() {
		return constNum;
	}
	public void setConstNum(int constNum) {
		this.constNum = constNum;
	}
	public String getConstName() {
		return constName;
	}
	public void setConstName(String constName) {
		this.constName = constName;
	}
	public String getConstWay() {
		return constWay;
	}
	public void setConstWay(String constWay) {
		this.constWay = constWay;
	}
	public String getConstArea() {
		return constArea;
	}
	public void setConstArea(String constArea) {
		this.constArea = constArea;
	}
	public String getConstPrice() {
		return constPrice;
	}
	public void setConstPrice(String constPrice) {
		this.constPrice = constPrice;
	}
	public String getConstLower() {
		return constLower;
	}
	public void setConstLower(String constLower) {
		this.constLower = constLower;
	}
	public String getConstOpening() {
		return constOpening;
	}
	public void setConstOpening(String constOpening) {
		this.constOpening = constOpening;
	}
	public String getConstInstitution() {
		return constInstitution;
	}
	public void setConstInstitution(String constInstitution) {
		this.constInstitution = constInstitution;
	}
	public String getConstPercent() {
		return constPercent;
	}
	public void setConstPercent(String constPercent) {
		this.constPercent = constPercent;
	}
	public String getCrtDate() {
		return crtDate;
	}
	public void setCrtDate(String crtDate) {
		this.crtDate = crtDate;
	}
	public String getUdtDate() {
		return udtDate;
	}
	public void setUdtDate(String udtDate) {
		this.udtDate = udtDate;
	}
	
	
}